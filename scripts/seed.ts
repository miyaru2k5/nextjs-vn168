async function hasData(table: any, label: string): Promise<boolean> {
  const result = await db.select({ c: count() }).from(table);
  const total = result[0]?.c ?? 0;
  if (total > 0) {
    console.log(`  ⏭  ${label}: already has ${total} records, skipping.`);
    return true;
  }
  return false;
}

async function seedCategories() {
  console.log('\n📁 Seeding Categories...');
  if (await hasData(categories, 'Categories')) return;

  const data = ARTICLE_CATEGORIES.map((cat, idx) => ({
    name: cat.name,
    slug: cat.slug,
    parent: idx > 3 && Math.random() > 0.7 ? pickRandom(ARTICLE_CATEGORIES.slice(0, 4)).name : null,
    status: Math.random() > 0.85 ? 'inactive' : 'active',
  }));

  await db.insert(categories).values(data);
  console.log(`  ✓ Inserted ${data.length} categories`);
}

