function ensureDir(dir: string) {
  if (!fs.existsSync(dir)) {
    fs.mkdirSync(dir, { recursive: true });
  }
}

function writeSeedJson(data: Record<string, any[] | Record<string, any>>) {
  // 1. Private seed-data (for server / gitignored)
  const privateDir = path.join(process.cwd(), 'seed-data');
  ensureDir(privateDir);

  // 2. Public seed-data (so client can fetch /seed-data/xxx.json in local dev)
  const publicDir = path.join(process.cwd(), 'public', 'seed-data');
  ensureDir(publicDir);

  Object.entries(data).forEach(([name, records]) => {
    const json = JSON.stringify(records, null, 2);
    const count = Array.isArray(records) ? records.length : (records && typeof records === 'object' ? Object.keys(records).length : 1);

    // Write to private
    const privatePath = path.join(privateDir, `${name}.json`);
    fs.writeFileSync(privatePath, json, 'utf-8');
    console.log(`  📄 Written ${count} item(s) → ${privatePath}`);

    // Also write to public/ for client-side fetch (very useful for local)
    const publicPath = path.join(publicDir, `${name}.json`);
    fs.writeFileSync(publicPath, json, 'utf-8');
  });

  // Write manifest in both places
  const manifest = {
    generatedAt: new Date().toISOString(),
    counts: Object.fromEntries(Object.entries(data).map(([k, v]) => [k, v.length])),
    note: 'Generated by scripts/seed.ts when SEED_TO_JSON=true (or auto in development). Use these instead of static mocks in local.',
  };
  const manifestJson = JSON.stringify(manifest, null, 2);

  fs.writeFileSync(path.join(privateDir, 'seed-manifest.json'), manifestJson, 'utf-8');
  fs.writeFileSync(path.join(publicDir, 'seed-manifest.json'), manifestJson, 'utf-8');

  console.log(`  📄 Written manifests to seed-data/ and public/seed-data/`);
}

