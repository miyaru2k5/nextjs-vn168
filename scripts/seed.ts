async function seedArticles() {
  console.log('\n📰 Seeding Articles...');
  if (await hasData(articles, 'Articles')) return;

  const allCategories = await db.select().from(categories);
  if (allCategories.length === 0) {
    console.log('  ⚠️  No categories found. Run categories seed first.');
    return;
  }

  const authors = ['Admin AIStarterKit', 'Nguyễn Thị Lan', 'Trần Văn Minh', 'Phạm Thu Hà', 'Lê Hoàng An'];

  const data = Array.from({ length: NUM_ARTICLES }).map((_, i) => {
    const cat = pickRandom(allCategories);
    const title = i < ARTICLE_TITLES.length ? ARTICLE_TITLES[i] : faker.lorem.sentence({ min: 8, max: 14 });
    const slugBase = title.toLowerCase().replace(/[^a-z0-9\s-]/g, '').replace(/\s+/g, '-').slice(0, 80);
    const slug = `${slugBase}-${1000 + i}`;

    const status = pickRandom([...ARTICLE_STATUSES]);
    const views = faker.number.int({ min: 80, max: 12400 });
    const featured = Math.random() > 0.75;

    return {
      title,
      slug,
      excerpt: pickRandom([
        'Khám phá cách tận dụng AI để tăng năng suất làm việc và sáng tạo nội dung chuyên nghiệp.',
        'Các chiến lược thực tế giúp sử dụng AI hiệu quả trong quy trình làm việc hàng ngày.',
        faker.lorem.paragraph({ min: 1, max: 2 }).slice(0, 180),
      ]),
      content: `<p>${faker.lorem.paragraphs(4, '<br/><br/>')}</p>`,
      categoryId: cat.id,
      author: pickRandom(authors),
      status,
      views,
      likes: faker.number.int({ min: 5, max: 420 }),
      readTime: faker.number.int({ min: 4, max: 14 }),
      featured,
      coverImage: generateImageUrl(`article-${slugBase}`, 1200, 630),
      publishedAt: status === 'published' ? faker.date.recent({ days: 90 }) : null,
      createdAt: faker.date.past({ years: 1 }),
    };
  });

  await db.insert(articles).values(data);
  console.log(`  ✓ Inserted ${data.length} articles`);
}

async function seedComments() {
  console.log('\n💬 Seeding Comments...');
  if (await hasData(comments, 'Comments')) return;

  const allArticles = await db.select().from(articles);
  if (allArticles.length === 0) {
    console.log('  ⚠️  No articles. Skipping comments.');
    return;
  }

  const data: any[] = [];

  for (let i = 0; i < NUM_COMMENTS; i++) {
    const article = pickRandom(allArticles);
    const name = generateVietnameseName();
    const statuses = ['approved', 'approved', 'approved', 'pending', 'hidden', 'reported'];

    data.push({
      articleId: article.id,
      authorName: name,
      authorAvatar: generateImageUrl(`comment-${name}`, 64, 64),
      content: faker.lorem.sentence({ min: 6, max: 18 }),
      status: pickRandom(statuses),
      likes: faker.number.int({ min: 0, max: 28 }),
      createdAt: faker.date.recent({ days: 45 }),
    });
  }

  await db.insert(comments).values(data);
  console.log(`  ✓ Inserted ${data.length} comments`);
}

